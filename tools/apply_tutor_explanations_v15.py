    extra = '''\n\n## V1.0 Beta 2 All Versions\n\n- V1.7A: 66문항 튜터형 해설 적용 완료\n- V1.7B: 40문항 튜터형 해설 적용 완료\n- V1.6: 40문항 튜터형 해설 적용 완료\n- V1.5: 40문항 튜터형 해설 적용 완료\n- 전체: 186문항 튜터형 해설 적용 완료\n\n원본 `question`, `answer`, `explanation` 필드는 보존했고, 학습용 필드 `learningExplanation` 및 화면 표시용 `questionBlocks`를 추가했습니다.\n'''
    if '## V1.0 Beta 2 All Versions' not in r:
        r += extra
    readme.write_text(r, encoding='utf-8')

