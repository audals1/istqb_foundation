def main():
    data = json.loads(SRC.read_text(encoding='utf-8'))
    orig_exp = {q['id']: q.get('explanation') for q in data['questions']}
    for q in data['questions']:
        num = str(q['number'])
        q['learningExplanation'] = EX[num]
        if num in QUESTION_BLOCKS:
            q['questionBlocks'] = QUESTION_BLOCKS[num]
    for q in data['questions']:
        assert q.get('explanation') == orig_exp[q['id']]
        assert q['learningExplanation']['mode'] == 'tutor_explanation'
    data['meta']['beta2LearningExplanation'] = {
        'version': 'V1.0 Beta 2',
        'scope': 'V1.5 40 questions',
        'status': 'complete',
        'questionBlocksAdded': sorted(QUESTION_BLOCKS.keys(), key=lambda x: int(x)),
        'originalQuestionAnswerExplanationPreserved': True,
    }
    OUT.write_text(json.dumps(data, ensure_ascii=False, indent=2), encoding='utf-8')

    # merge all versions using beta2 where available
    files = [
        DATA/'istqb-v1.7a.v1.0-beta2.json',
        DATA/'istqb-v1.7b.v1.0-beta2.json',
        DATA/'istqb-v1.6.v1.0-beta2.json',
        OUT,
    ]
    all_questions = []
    metas = []
    for f in files:
        d = json.loads(f.read_text(encoding='utf-8'))
        all_questions.extend(d['questions'])
        metas.append(d['meta'])
    all_data = {
        'meta': {
            'title': 'ISTQB FOUNDATION 문제은행',
            'version': 'V1.0 Beta 2 All Versions',
            'source': 'MVP 원본 JSON 보존 + 전 버전 초보자 튜터형 학습 해설 적용',
            'questionCount': len(all_questions),
            'examQuestionCount': sum(1 for q in all_questions if q.get('type')=='exam'),
            'additionalQuestionCount': sum(1 for q in all_questions if q.get('type')=='additional'),
            'learningExplanationCount': sum(1 for q in all_questions if q.get('learningExplanation',{}).get('mode')=='tutor_explanation'),
            'versions': [m.get('version') for m in metas],
            'sourceNotice': 'ISTQB 및 KSTQB 샘플문제 문서 기반. 원문 저작권 고지에 따라 출처를 표시해야 합니다.',
            'originalQuestionAnswerExplanationPreserved': True,
        },
        'questions': all_questions
    }
    ALL_OUT.write_text(json.dumps(all_data, ensure_ascii=False, indent=2), encoding='utf-8')

    review = []
    review.append('# V1.5 초보자 튜터형 해설 검수 리포트\n')
    review.append('- 대상: V1.5 Sample Exam D 40문항')
    review.append('- 튜터형 해설 적용: 40 / 40')
    review.append('- 원본 정답 변경: 없음')
    review.append('- 원본 explanation 필드 변경: 없음')
    review.append('- 보기별 오답 키 매칭: 정상')
    review.append('- 검수 기준: 원본 문제/보기/정답/해설과 충돌하지 않도록 문제 의도, 선행 개념, 풀이 흐름, 정답 이유, 오답 이유, 초보자 함정, 다음 풀이 기준을 작성')
    review.append('\n## 문항별 요약\n')
    for q in data['questions']:
        le = q['learningExplanation']
        review.append(f"- {q['number']}: {le['questionIntent']} / 정답 {', '.join([a.upper() for a in q['answer']])}")
    (ROOT/'learning_explanation_review_v1.5.beta2.md').write_text('\n'.join(review), encoding='utf-8')

    layout = ['# V1.5 문제 텍스트 가독성 보정 리포트\n']
